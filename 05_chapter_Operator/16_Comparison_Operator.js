// Comparison operators

// == , ===
// ==  -> loose Comparion (checks eitehr value or data type)
console.log(5 == "5") // True
console.log(5 === "5") // False
// === -> strict comparison (checks the literals for both data type and value)
// >, < , >=, <=

console.log(5 != "5"); //false  lose -> datatype or value is checked
console.log(5 !== "5"); // true

console.log (3 == "3");